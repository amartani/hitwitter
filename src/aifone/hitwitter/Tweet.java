package aifone.hitwitter;

public class Tweet {
	private String autor;
	private String conteudo;
	
	public Tweet(String autor, String conteudo) {
		super();
		this.autor = autor;
		this.conteudo = conteudo;
	}

	public String getAutor() {
		return autor;
	}

	public String getConteudo() {
		return conteudo;
	}
	
	
}
